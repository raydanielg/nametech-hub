<script>
    document.addEventListener('DOMContentLoaded', function() {
        const searchInput = document.getElementById('searchInput');
        const resultsList = document.getElementById('resultsList');
        const searchResults = document.getElementById('searchResults');
        const searchPlaceholder = document.getElementById('searchPlaceholder');
        const searchLoading = document.getElementById('searchLoading');
        let timeout = null;

        searchInput.addEventListener('input', function() {
            const query = this.value.trim();
            
            if (query.length < 2) {
                searchResults.classList.add('d-none');
                searchPlaceholder.classList.remove('d-none');
                searchLoading.classList.add('d-none');
                return;
            }

            // Show loading
            searchPlaceholder.classList.add('d-none');
            searchLoading.classList.remove('d-none');
            searchResults.classList.add('d-none');

            clearTimeout(timeout);
            timeout = setTimeout(() => {
                // AJAX Call
                fetch(`/api/search?q=${encodeURIComponent(query)}`)
                    .then(response => response.json())
                    .then(data => {
                        searchLoading.classList.add('d-none');
                        resultsList.innerHTML = '';
                        
                        if (data.results && data.results.length > 0) {
                            data.results.forEach(item => {
                                resultsList.innerHTML += `
                                    <a href="${item.url}" class="list-group-item list-group-item-action py-3 px-0 border-0 border-bottom">
                                        <div class="d-flex justify-content-between align-items-center">
                                            <div>
                                                <div class="fw-bold text-dark">${item.title}</div>
                                                <div class="small text-muted">${item.description}</div>
                                            </div>
                                            <span class="badge bg-success bg-opacity-10 text-success rounded-pill px-2 py-1 x-small">${item.type}</span>
                                        </div>
                                    </a>
                                `;
                            });
                            searchResults.classList.remove('d-none');
                        } else {
                            resultsList.innerHTML = '<div class="text-center py-4 text-muted">No results found for "' + query + '"</div>';
                            searchResults.classList.remove('d-none');
                        }
                    })
                    .catch(error => {
                        console.error('Search error:', error);
                        searchLoading.classList.add('d-none');
                    });
            }, 300);
        });

        // Focus input when modal opens
        const searchModal = document.getElementById('searchModal');
        searchModal.addEventListener('shown.bs.modal', function () {
            searchInput.focus();
        });
    });
</script>
